#include "../cub3d.h"

int	map_value(char c)
{
	int	v;

	if (c == ' ')
		v = -1;
	else if (c == 'N')
		v = 2;
	else if (c == 'S')
		v = 3;
	else if (c == 'E')
		v = 4;
	else if (c == 'W')
		v = 5;
	else if (c == '1')
		v = 1;
	else if (c == '0')
		v = 0;
	else
		v = -9;

	return (v);
}
